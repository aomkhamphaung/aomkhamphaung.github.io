#include<iostream>
using namespace std;
int main(){
    int a,b;
    a = 4;
    b = 2;

    cout<<a<<" - "<<b<<" = "<<(a - b)<<endl;
    cout<<a<<" + "<<b<<" = "<<(a + b)<<endl;
    cout<<a<<" * "<<b<<" = "<<(a * b)<<endl;
    cout<<a<<" / "<<b<<" = "<<(a / b)<<endl;
    cout<<a<<" % "<<b<<" = "<<(a % b)<<endl;
}